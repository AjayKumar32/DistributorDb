              <li>These tables have to be updated each time POS, Inventory or Debits Files are uploaded through the import screen.</li>
              <li>Check for new records button is to check for new records to be added into the clean tables.</li>
              <li>If new records exist in the check for new records screen, enter the cleanup values for them using the edit icon.</li>
              <li>Using the Import screen, excel files can be uploaded.This will append the new data to the previous data.</li>
              <li>Using the Export screen, records can be exported to an Excel file.</li>
              <li>Using the Add screen, records can be manually added to the tables.</li>
              <li>Using the Delete screen, selected records are deleted permanently. Admins have privilege to delete records.</li>
              <li>Delete log screen has the deleted records timestamp. </li>
              <li>Upload log screen has the Uploaded files timestamp. </li>
              
            </ul>

            <h2> Transactions </h2>
            <ul>
              <li>POS transactionsa, Inventory transactions and Debits can be viewed in this.</li>
              <li>Filters on Distributor,sales region, sales territory and on report date can be applied to filter the data.</li>
              <li>Using the Import screen, new files can be uploaded. This will append the new data to the previous data in the respective Load tables.</li>
              <li>After loading the data into Load table, these records can be cleanaed using the Clean Button, then the cleaned records are moved to the Cleaned tables.</li>
              <li>Then using the Calculate Button, derived costs are calculated and inserted into the final tables which are displayed in the transactions view.</li>
              <li>Using the Export screen, records are exported to an Excel file.</li>
              <li>Using the Add screen, records can be manually added to the tables.</li>
              <li>Using the Delete screen, selected records are deleted permanently. Admins have privilege to delete records.</li>
